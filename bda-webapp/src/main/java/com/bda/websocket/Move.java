package com.bda.websocket;

public class Move {

	  private int x;
	  private int y;
	  private Slot player;

	  public int getX() {
	    return x;
	  }

	  public void setX(int x) {
	    this.x = x;
	  }

	  public int getY() {
	    return y;
	  }

	  public void setY(int y) {
	    this.y = y;
	  }

	  public Slot getPlayer() {
	    return player;
	  }

	  public void setPlayer(Slot player) {
	    this.player = player;
	  }
	}
